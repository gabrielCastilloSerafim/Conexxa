import SwiftUI

struct ConexxaColor {
    
    /// Hex: #ffffff
    static func white() -> Color {
        Color(hex: "#ffffff")
    }
    
    /// Hex: #F2F2F2
    static func dirtyWhite() -> Color {
        Color(hex: "#F2F2F2")
    }
    
    /// Hex: #0D0D0D
    static func black() -> Color {
        Color(hex: "#0D0D0D")
    }
    
    /// Hex: #75D966
    static func green() -> Color {
        Color(hex: "#75D966")
    }
    
    /// Hex: #4E35F2
    static func blue() -> Color {
        Color(hex: "#4E35F2")
    }
    
    /// Hex: #614ED9
    static func purple() -> Color {
        Color(hex: "#614ED9")
    }
    
    /// Hex: #D3D3D3
    static func lightGray() -> Color {
        Color(hex: "#D3D3D3")
    }
}


